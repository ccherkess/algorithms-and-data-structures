2 лабораторная работа
Сортировки

— Реализовать сортировку Timsort.
Сделать это необходимо на базе класса динамического массива(или списка), созданного в первой лабораторной.
Соответственно, на входе в программу массив должен быть пустым (на выбор: сгенерировать рандомно или создать ручками свой). Должна быть возможность с ним взаимодействовать (добавлять/удалять элементы, просматривать массив и его размерность) и, соответственно, сортировать указанным методом.

Доп.задание:
— Вместе с Timsort реализовать любую другую сортировку на выбор 
— Реализовать следующее: после того, как программа отсортирует введённый с консоли массив обоими способами, вывести время работы обеих сортировок в таких у.е., чтобы их можно было сравнить между собой (и осознать, почему та или иная сортировка работает быстрее)